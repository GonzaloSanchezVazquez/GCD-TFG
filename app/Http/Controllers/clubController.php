    {
        //

        $club = new Club;

        $club->nombre = $request->nombre_club;
        $club->email = $request->email_club;
        $club->password = $request->pass_club;
        $club->localidad = $request->localidad_club;
        $club->telefono = $request->telefono_club;
        $club->CIF = $request->cif_club;

        $club->save();
    }

